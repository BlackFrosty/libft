#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*pdst;
	unsigned char	*psrc;

	pdst = (unsigned char *)dst;
	psrc = (unsigned char *)src;
	while (n-- && *psrc)
		if ((*pdst++ = *psrc++) == (unsigned char)c)
			return (pdst);
	return (NULL);
}
